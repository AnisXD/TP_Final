CREATE Procedure ObtenerTEST
(
@DNIUSU varchar(9)
)
AS
SELECT 
DNI_USU					AS DNI,
NOMBRE_USU				AS NOMBRE
FROM 
USUARIOS
WHERE ESTADO_USU = 1
AND DNI_USU = @DNIUSU

create procedure ModificarTEST
(
@DNI varchar(9),
@NOMBRE varchar(30)
)
AS
UPDATE
USUARIOS
SET
NOMBRE_USU = @NOMBRE
WHERE DNI_USU = @DNI